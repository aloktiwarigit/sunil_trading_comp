// Same bootstrap sequence as customer_app/main.dart, except:
//   - shopId is resolved from the Google operator's custom claim after
//     sign-in, NOT hardcoded at compile time. Until S4.1 lands (Sprint 3),
//     we use the flagship shopId as the default.
//   - Auth flow is Google Sign-In (AuthTier.googleOperator), NOT anonymous.
